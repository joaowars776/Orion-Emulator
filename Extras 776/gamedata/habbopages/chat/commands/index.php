<p class="bullet-item">·&nbsp;<b>:push X</b> - Knuffa en annan användare.<br/>
<p class="bullet-item">·&nbsp;<b>:pull X</b> - Dra åt dig en annan användare.<br/>
<p class="bullet-item">·&nbsp;<b>:redeem</b> - Växla.<br/>
<p class="bullet-item">·&nbsp;<b>:faceless</b> - Ta bort ditt ansikte.<br/>
<p class="bullet-item">·&nbsp;<b>:empty Yes</b> - Töm ditt inventory på möbler.<br/>
<p class="bullet-item">·&nbsp;<b>:kiss X</b> - Kyss en annan användare<br/>
<p class="bullet-item">·&nbsp;<b>:hug X</b> - Krama en annan användare<br/>
<p class="bullet-item">·&nbsp;<b>:kill X</b> - Döda en annan användare<br/>
