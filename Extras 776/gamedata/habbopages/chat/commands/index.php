<p class="bullet-item">·&nbsp;<b>:sit</b> - Gör så att du sitter ner.<br/>
<p class="bullet-item">·&nbsp;<b>:lay</b> - Gör så att du ligger ner.<br/>
<p class="bullet-item">·&nbsp;<b>:pickall</b> - Plockar upp alla möbler i ditt rum.<br/>
<p class="bullet-item">·&nbsp;<b>:ejectall</b> - Plockar upp alla möblerna i ditt rum<br/>
