<span class="padding-top"><br></span><h1>Special chat commands</h1><span class="padding-top"><br></span>
<span class="padding-top"><br></span><p>There a lots of clever ways to enhance your chat in Habbo - Learn them all, or just a couple, but they''re all here in one handy list just for you!</p><span class="padding-bottom"><br></span>
