<p class="bullet-item">·&nbsp;<b>:afk</b> - Du somnar<br/>
<p class="bullet-item">·&nbsp;<b>:pistol</b> - Du får en pistol<br/>
<p class="bullet-item">·&nbsp;<b>:weed</b> - Du röker (ta det med en nypa salt)<br/>
<p class="bullet-item">·&nbsp;<b>:snus</b> - Du tar en snus (ta det med en nypa salt)<br/>
